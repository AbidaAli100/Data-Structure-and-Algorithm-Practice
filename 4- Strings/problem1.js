/*Traverse an array [4, -20, 50, 1, 0, -5, 6, 7, 10] 
and print all of its elements and indices. */

let array = [4, -20, 50, 1, 0, -5, 6, 7, 10];

// for loop

for (let i = 0; i < array.length; i++) {
    console.log(array[i]);
    console.log(array.length);
}

let i = 0;
while (i < array.length) {
    console.log(array[i]);
    console.log(array.length);
    i++;
}